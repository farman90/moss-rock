<section>
    <h2>Planning Your Moss Rock Retaining Wall</h2>
    <p>Every successful retaining wall construction project starts with good planning. Here’s what to consider:</p>
    <ul>
        <li><strong>Select the Location:</strong> Common sites include the base of slopes, garden terraces, or around flower beds. The wall should serve both functional purposes such as holding soil and decorative purposes.</li>
        <li><strong>Check Local Building Codes:</strong> Many municipalities require permits for walls taller than 3 to 4 feet. DIY projects should usually be limited to 3 feet or less for safety.</li>
        <li><strong>Estimate Material Needs:</strong> On average, 1 ton of moss rock covers 35 to 40 square feet of wall face at 8 inches thick. For example, a wall 20 feet long by 2 feet tall, which is 40 square feet, would need about 1 ton of stone.</li>
        <li><strong>Design for Stability:</strong> Retaining walls need a backward slope, known as batter, of about 1 inch per foot of height to resist soil pressure.</li>
    </ul>
</section>

<section>
    <h2>Tools and Materials</h2>
    <ul>
        <li>Moss rock boulders, including large ones for the base and medium and small ones for upper rows</li>
        <li>Crushed gravel or road base for drainage</li>
        <li>Shovel, rake, and wheelbarrow</li>
        <li>String line and level</li>
        <li>Work gloves, safety boots, and eye protection</li>
        <li>Optional: Landscape fabric for soil separation and drainage management</li>
