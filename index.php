<section>
    <h2>Step-by-Step Guide to Building a Moss Rock Retaining Wall</h2>
    <ol class="steps-list">
        <li><strong>Excavate the Base:</strong> Dig a trench about 6 to 8 inches deep and twice as wide as your largest stones. A solid base is critical for stability.</li>
        <li><strong>Lay the Gravel Foundation:</strong> Fill the trench with 4 to 6 inches of compacted gravel. Good drainage prevents hydrostatic pressure, which can push walls outward over time.</li>
        <li><strong>Place the First Course of Moss Rock:</strong> Set the largest, flattest boulders at the bottom. This foundation layer carries the weight of the wall, so take time to position each stone securely.</li>
        <li><strong>Stack Successive Rows:</strong> Stagger vertical joints to avoid weak spots. Lean each stone slightly backward into the slope. Fill small gaps with smaller stones to lock larger rocks in place.</li>
        <li><strong>Backfill as You Build:</strong> After every row, backfill with gravel or soil behind the wall. Compacting the fill adds support and improves drainage.</li>
        <li><strong>Cap the Wall:</strong> Finish with wide, flat stones as capstones. These lock the structure together and provide a finished appearance.</li>
    </ol>
</section>

